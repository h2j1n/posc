package get_study_user.vo;

public class ProcessVO {
	private int pro_no;
	private String pro_date;
	private int pro_res;
	private int ap_no;
	
	
	public int getPro_no() {
		return pro_no;
	}
	public void setPro_no(int pro_no) {
		this.pro_no = pro_no;
	}
	public String getPro_date() {
		return pro_date;
	}
	public void setPro_date(String pro_date) {
		this.pro_date = pro_date;
	}
	public int getPro_res() {
		return pro_res;
	}
	public void setPro_res(int pro_res) {
		this.pro_res = pro_res;
	}
	public int getAp_no() {
		return ap_no;
	}
	public void setAp_no(int ap_no) {
		this.ap_no = ap_no;
	}
	
	
}
